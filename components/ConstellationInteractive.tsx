export { constellations };
export type { Constellation };

interface ConstellationInteractiveProps {
  viewBox: string;
  onHover?: (constellation: Constellation | null) => void;
  onSelect?: (constellation: Constellation) => void;
}

export function ConstellationInteractive({ viewBox, onHover, onSelect }: ConstellationInteractiveProps) {
  const [hoveredId, setHoveredId] = useState<string | null>(null);

  const handleMouseEnter = (constellation: Constellation) => {
    setHoveredId(constellation.id);
    if (onHover) onHover(constellation);
  };

  const handleMouseLeave = () => {
    setHoveredId(null);
    if (onHover) onHover(null);
  };

  const handleClick = (constellation: Constellation) => {
    if (onSelect) onSelect(constellation);
  };

  return (
    <svg 
      width="100%" 
      height="auto" 
      viewBox={viewBox} 
      xmlns="http://www.w3.org/2000/svg"
      style={{ position: 'absolute', top: 0, left: 0, pointerEvents: 'none' }}
    >
      <defs>
        <filter id="glow">
          <feGaussianBlur stdDeviation="2" result="coloredBlur"/>
          <feMerge>
            <feMergeNode in="coloredBlur"/>
            <feMergeNode in="SourceGraphic"/>
          </feMerge>
        </filter>
      </defs>

      <g style={{ pointerEvents: 'auto' }}>
        {constellations.map((constellation) => {
          const isHovered = hoveredId === constellation.id;
          const linesArray = Array.isArray(constellation.lines) 
            ? constellation.lines 
            : [constellation.lines];
          
          return (
            <g
              key={constellation.id}
              onMouseEnter={() => handleMouseEnter(constellation)}
              onMouseLeave={handleMouseLeave}
              onClick={() => handleClick(constellation)}
              style={{ cursor: 'pointer' }}
            >
              {linesArray.map((linePoints, index) => (
                <g key={`${constellation.id}-line-${index}`}>
                  {/* Invisible larger hit area */}
                  <polyline
                    points={linePoints}
                    fill="none"
                    stroke="transparent"
                    strokeWidth="20"
                    strokeLinecap="round"
                    strokeLinejoin="round"
                  />
                  
                  {/* Visible constellation lines */}
                  <polyline
                    points={linePoints}
                    fill="none"
                    stroke={isHovered ? '#FFD700' : 'transparent'}
                    strokeWidth={isHovered ? '2' : '0'}
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    style={{
                      transition: 'all 0.3s ease',
                      filter: isHovered ? 'url(#glow)' : 'none'
                    }}
                  />
                </g>
              ))}
            </g>
          );
        })}
      </g>
    </svg>
  );
}